 import java.util.Scanner;

public class for_8393{
    static Scanner scanner = new Scanner(System.in);
    public static void main(String[] args){
       int a = scanner.nextInt();
       int sum=0;
        for(int i=0; i<=a; i++){
            sum=sum+i;
        }

        System.out.println(sum);

    }



}
